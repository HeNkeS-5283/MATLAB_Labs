disp('Задайте розмірність масиву')
x = input('x = ');
disp('Введіть значення для масиву:')
a = zeros(1,x);
for i = 1:x
   a(i) = input(sprintf('Введіть елемент [%d]: ', i));
end
disp('Введений масив а:')
a
for i = 1:x
   if (a(i)>0) a(i)=1;
   else a(i)=0;
   end
end
disp('Введений масив а, з заміненими числами додатніх на одиницю відємних на нуль:')
a