disp('Розвязання системи рівнянь з допомогою функції linsolve:')
disp('Перша система рівнянь:')
a1 = [2 -1 1 1; 1 2 -1 4; 1 7 -4 11]
b1 = [0;0;0]

x1 = linsolve(a1,b1);
disp('Невідомі рівняння')
disp(x1)

disp('Друга система рівнянь:')
a2 = [2 -1 -1 -2; -1 1 1 24; 7 -5 -2 -4; -3 2 1 2; 1 -2 1 2]
b2 = [1;1;8;3;3]

x2 = linsolve(a2,b2);
